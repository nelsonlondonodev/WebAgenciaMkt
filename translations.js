function setLanguage(lang) {
  const langIndicator = document.getElementById("langIndicator");
  if (!langIndicator) {
    console.error(
      "CRITICAL: El elemento #langIndicator no existe en el HTML."
    );
    return;
  }
  localStorage.setItem("language", lang);
  document.documentElement.lang = lang;
  document.body.setAttribute("data-lang", lang);
  langIndicator.innerHTML =
    lang === "es"
      ? '<span role="img" aria-label="Bandera de Reino Unido">üá¨üáß</span> EN'
      : '<span role="img" aria-label="Bandera de Espa√±a">üá™üá∏</span> ES';

  let translationsUrl;
  if (window.location.pathname.includes("articulo-seo-post-fiestas.html")) {
    translationsUrl = "./translations/articulo-seo-post-fiestas.json";
  } else if (window.location.pathname.includes("articulo-ejemplo.html")) {
    // TODO: Create a translations file for the example article
  } else if (window.location.pathname.includes("blog.html")) {
    translationsUrl = "./translations/blog.json";
  } else {
    // Default translations for other pages
    // You might want to create a main translations file
  }

  if (translationsUrl) {
    fetch(translationsUrl)
      .then((response) => response.json())
      .then((articleTranslations) => {
        const mergedTranslations = {
          es: { ...translations.es, ...articleTranslations.es },
          en: { ...translations.en, ...articleTranslations.en },
        };
        document.querySelectorAll("[data-translate-key]").forEach((el) => {
          const key = el.getAttribute("data-translate-key");
          const translation = mergedTranslations[lang]?.[key];
          if (translation) {
            if (key === "articleContent") {
              el.innerHTML = "";
              translation.forEach((item) => {
                const newEl = document.createElement(item.type);
                if (item.type === "ul") {
                  item.items.forEach((liContent) => {
                    const li = document.createElement("li");
                    li.innerHTML = liContent;
                    newEl.appendChild(li);
                  });
                } else {
                  newEl.innerHTML = item.content;
                }
                el.appendChild(newEl);
              });
            } else {
              el.innerHTML = translation;
            }
          }
        });
      });
  } else {
    // Handle translations for pages without a dedicated JSON file
    document.querySelectorAll("[data-translate-key]").forEach((el) => {
      const key = el.getAttribute("data-translate-key");
      const translation = translations[lang]?.[key];
      if (translation) {
        if (
          key.includes("Content") ||
          key.includes("Button") ||
          key === "footerMadeWith" ||
          key === "footerText"
        ) {
          el.innerHTML = translation;
        } else {
          el.textContent = translation;
        }
      }
    });
  }
}

export function initTranslations() {
    const languageSwitcher = document.getElementById("languageSwitcher");
    if (languageSwitcher) {
        let currentLang = localStorage.getItem("language") || "es";
        languageSwitcher.addEventListener("click", () => {
            const newLang = currentLang === "es" ? "en" : "es";
            setLanguage(newLang);
            currentLang = newLang;
        });
        setLanguage(currentLang);
    }
}
